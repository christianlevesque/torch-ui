namespace TorchUI.Bootstrap;

/// <summary>
/// Represents the available toggle types in Bootstrap
/// </summary>
public enum Toggle
{
	/// <summary>
	/// Toggles the active state on a button
	/// </summary>
	Button,

	/// <summary>
	/// Toggles collapsible content
	/// </summary>
	Collapse,

	/// <summary>
	/// Toggles a dropdown menu
	/// </summary>
	Dropdown,

	/// <summary>
	/// Toggles a modal
	/// </summary>
	Modal,

	/// <summary>
	/// Toggles an offcanvas sidebar
	/// </summary>
	Offcanvas,

	/// <summary>
	/// Toggles a popover
	/// </summary>
	Popover,

	/// <summary>
	/// Switches between tabs
	/// </summary>
	Tab,

	/// <summary>
	/// Toggles a tooltip
	/// </summary>
	Tooltip
}
