namespace TorchUI.Bootstrap;

/// <summary>
/// Represents the available theme colors in Bootstrap
/// </summary>
public enum ThemeColor
{
	/// <summary>
	/// Represents the Bootstrap <c>primary</c> theme color
	/// </summary>
	Primary,

	/// <summary>
	/// Represents the Bootstrap <c>secondary</c> theme color
	/// </summary>
	Secondary,

	/// <summary>
	/// Represents the Bootstrap <c>success</c> theme color
	/// </summary>
	Success,

	/// <summary>
	/// Represents the Bootstrap <c>danger</c> theme color
	/// </summary>
	Danger,

	/// <summary>
	/// Represents the Bootstrap <c>warning</c> theme color
	/// </summary>
	Warning,

	/// <summary>
	/// Represents the Bootstrap <c>info</c> theme color
	/// </summary>
	Info,

	/// <summary>
	/// Represents the Bootstrap <c>light</c> theme color
	/// </summary>
	Light,

	/// <summary>
	/// Represents the Bootstrap <c>dark</c> theme color
	/// </summary>
	Dark
}
