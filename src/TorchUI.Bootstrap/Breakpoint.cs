namespace TorchUI.Bootstrap;

/// <summary>
/// Represents the available breakpoints in Bootstrap
/// </summary>
public enum Breakpoint
{
	/// <summary>
	/// Represents the default breakpoint in Bootstrap, <c>&lt;576px</c>
	/// </summary>
	Xs,

	/// <summary>
	/// Represents the small breakpoint in Bootstrap, <c>&ge;576px</c> and <c>&lt;768px</c>
	/// </summary>
	Sm,

	/// <summary>
	/// Represents the medium breakpoint in Bootstrap, <c>&ge;768px</c> and <c>&lt;992px</c>
	/// </summary>
	Md,

	/// <summary>
	/// Represents the large breakpoint in Bootstrap, <c>&ge;992px</c> and <c>&lt;1200px</c>
	/// </summary>
	Lg,

	/// <summary>
	/// Represents the extra-large breakpoint in Bootstrap, <c>&ge;1200px</c> and <c>&lt;1400px</c>
	/// </summary>
	Xl,


	/// <summary>
	/// Represents the extra-extra-large breakpoint in Bootstrap, <c>&ge;1400px</c>
	/// </summary>
	Xxl
}
